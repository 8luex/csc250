    private void btSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSubmitActionPerformed
        Authority a;
        try {
            a = AuthorityDA.checkAuthorityId(jTextField1.getText());
        } catch (Exception e) {
            JLabel label = new JLabel("ไม่พบรหัสเจ้าหน้าที่นี้ในระบบ");
            label.setFont(new Font("Leelawadee", 0, 14));
            JOptionPane.showMessageDialog(null, label,"แจ้งเตือน", JOptionPane.ERROR_MESSAGE);
            return;
        }
            boolean status = true;
            if(status) {
                CallPaymentSlip cpmsl = new CallPaymentSlip(a);
                cpmsl.setVisible(true);
            } else {
                JOptionPane.showMessageDialog(this, "Fail - Try again", "ไม่มีรหัสนี้อยู่ในฐานข้อมูล", JOptionPane.WARNING_MESSAGE);
            }
    }//GEN-LAST:event_btSubmitActionPerformed

