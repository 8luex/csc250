    private void btSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSubmitActionPerformed
        // TODO add your handling code here:
        String patiID = (String)jComboBox1.getSelectedItem();
        Patient patient = null;
        String historyID = null;
        
        try {
            patient = Patient.FindPatient(patiID);
            historyID = (String)jComboBox2.getSelectedItem();
        } catch (Exception ex) {
            System.out.println("Error: " + ex.getMessage());
            return;
        }
        ArrayList<TreatmentHistory> histories = new ArrayList<>();
        histories = patient.getAllPatienttreatmenthistory();
        TreatmentHistory history = null;

        for(int i=0; i<histories.size(); i++) {
            if (historyID.equals(histories.get(i).getID())) {
                history = histories.get(i);
            }
        }
        try {
            PaymentSlip paymentSlip = new PaymentSlip(history.getTreatmentList(), history.getPrescription());
            history.setPaymentSlip(paymentSlip);
            Patient.terminate();
        } catch (Exception e) {
            System.out.println("Error: " + e);
            return;
        }
        
        
            boolean status = true;
            if(status) {
                DisplayPaymentSlip dpsl = new DisplayPaymentSlip(patient, history, authority);
                dpsl.setVisible(true);
            } else {
                JOptionPane.showMessageDialog(this, "Fail - Try again", "ไม่มีรหัสนี้อยู่ในฐานข้อมูล", JOptionPane.WARNING_MESSAGE);
            }
    }//GEN-LAST:event_btSubmitActionPerformed

